class Node:
	def __init__(self):
		self.nodeId = 0		#Self Value
		self.nodeName = ''	#Self Value

	def GetNodeId(self):
		return self.nodeId

	def SetNodeId(self, nodeId):
		self.nodeId = nodeId

	def GetNodeName(self):
		return self.nodeName

	def SetNodeName(self, nodeName):
		self.nodeName = nodeName